package kodlamaio;

public class conditionals {

	public static void main(String[] args) {
		
		int num = 15;
		
		if (num <20) {
			System.out.println("Number is less than 20.");
		}
		else if (num ==20) {
			System.out.println("Number is equal to 20.");
		}
		else {
			System.out.println("Number is greater than 20.");
		}
	}

}
